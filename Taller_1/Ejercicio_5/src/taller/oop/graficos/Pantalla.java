        private void setValues() {
            this.numeroPiezaField.setText(facturas.get(rowValue).getNumeroPieza());
            this.descripcionField.setText(facturas.get(rowValue).getDescripcion());
            this.cantidadField.setText(""+facturas.get(rowValue).getCantidad());
            this.precioField.setText(""+facturas.get(rowValue).getPrecioUnitario());
        }
        
        private void modificarBtnActionPerformed(java.awt.event.ActionEvent evt) {                                             
            
            try{
                if (!("".equals(this.numeroPiezaField.getText()) || "".equals(this.descripcionField.getText()))) {
                    int cantidad = "".equals(this.cantidadField.getText())?0:Integer.parseInt(this.cantidadField.getText());
                    double precio = "".equals(this.precioField.getText())?0:Double.parseDouble(this.precioField.getText());

                    facturas.remove(rowValue);

                    facturas.add(rowValue,new Factura(
                            this.numeroPiezaField.getText(), this.descripcionField.getText(),
                            cantidad, precio
                    ));

                    setTabla();
                    cleanText();
                    this.dispose();
                } else {
                    throw new NullPointerException();
                }
            } catch (Exception e){
                JOptionPane.showMessageDialog(null, "Los valores ingresados no son válidos.",
                            "Error al agregar factura.",JOptionPane.ERROR_MESSAGE);
            }
            
        }                                            

        private void cancelarBtnActionPerformed(java.awt.event.ActionEvent evt) {                                            
            this.dispose();
        }                                           

        

