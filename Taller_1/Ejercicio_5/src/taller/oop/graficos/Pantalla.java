    private void cleanText() {
        numeroPiezaField.setText("");
        descripcionField.setText("");
        cantidadField.setText("");
        precioField.setText("");
    }
    
    
    private void fillData() {
        this.data = new ArrayList<Object[]>();
        this.contador = 1;
        for (Factura f : this.facturas) {
            this.data.add(f.toArray(contador));
            contador++;
        }
    }

    
    private void setTabla() {
        fillData();
        try {
            Object[][] dataset = new Object[this.data.size()][5];
            for (int i = 0; i < dataset.length; i++) {
                dataset[i] = this.data.get(i);
            }
            tabla.setModel(new javax.swing.table.DefaultTableModel(
                dataset,
                new String [] {
                    "N°", "Numero de Pieza", "Descripcion", "Cantidad", "Precio", "Monto Total"
                }
            ) {
                Class[] types = new Class [] {
                    Integer.class, String.class, String.class, Integer.class, Double.class, Double.class
                };

                public Class getColumnClass(int columnIndex) {
                    return types [columnIndex];
                }
            });
        }catch(Exception e) {
            
        }       
    }
       
    
